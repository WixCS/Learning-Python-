# All data types

# Integer data type
print("This is integer")
a = 16
print(a)

# Floating point number data type
print("This is Floating point number")
x = 321.216
print(x)

# String data type
print("This is String")
str = "Prince"
print(str)

# Boolean data type 
print("This is boolean")
b = True
print(b)

# None data type 
print("This is None")
n = None
print(n)